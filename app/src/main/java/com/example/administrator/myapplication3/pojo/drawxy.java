public class drawxy {

    private int x;
    private int y;

    public float getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "drawxy{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
